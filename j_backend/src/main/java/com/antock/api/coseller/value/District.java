    private final String value;
    private final String city;

    District(String value, String city) {
        this.value = value;
        this.city = city;
    }

    @JsonValue
    public String getValue() {
        return value;
    }

    public String getCity() {
        return city;
    }

    @JsonCreator
    public static District fromValue(String value) {
        for (District district : District.values()) {
            if (district.value.equalsIgnoreCase(value)) {
                return district;
            }
        }
        throw new IllegalArgumentException("Unknown district: " + value);
    }

    public static List<District> getDistrictsByCity(String cityName) {
        List<District> result = new ArrayList<>();
        for (District district : District.values()) {
            if (district.city.equals(cityName)) {
                result.add(district);
            }
        }
        return result;
    }
